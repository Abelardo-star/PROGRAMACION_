public class Actividad1 {
    public static void main(String[] args) {
        int x = 144;
        int y =  999;
        System.out.println("El Valor de x: " + x);
        System.out.println("El Valor de y: " + y);
        int suma = x + y;
        System.out.println("Los dos números sumados da: " + suma);
        int resta = x - y;
        System.out.println("Los dos números restados da: " + resta);
        int multiplicacion = x * y;
        System.out.println("Los dos números multiplicados da: " + multiplicacion);
        double division = (double) x / (double) y;
        System.out.println("Los dos números divididos da: " + division);
    }
}
